public class ModArquivoVendedor {
    private String codVendedor;
    private String nomeVendedor;
    private String claVendedor;
    private String operadorVende;
    private String bloqueioVende;

    public String getCodVendedor() {
        return codVendedor;
    }

    public void setCodVendedor(String codVendedor) {
        this.codVendedor = codVendedor;
    }

    public String getNomeVendedor() {
        return nomeVendedor;
    }

    public void setNomeVendedor(String nomeVendedor) {
        this.nomeVendedor = nomeVendedor;
    }

    public String getClaVendedor() {
        return claVendedor;
    }

    public void setClaVendedor(String claVendedor) {
        this.claVendedor = claVendedor;
    }

    public String getOperadorVende() {
        return operadorVende;
    }

    public void setOperadorVende(String operadorVende) {
        this.operadorVende = operadorVende;
    }

    public String getBloqueioVende() {
        return bloqueioVende;
    }

    public void setBloqueioVende(String bloqueioVende) {
        this.bloqueioVende = bloqueioVende;
    }
    
    
}
