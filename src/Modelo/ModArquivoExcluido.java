public class ModArquivoExcluido {
    private String dataExclu;
    private String barras;
    private String codigo;
    private String nome;
    private String unitario;
    private String classFisc;
    private String icm;
    private String gnr;

    public String getDataExclu() {
        return dataExclu;
    }

    public void setDataExclu(String dataExclu) {
        this.dataExclu = dataExclu;
    }

    public String getBarras() {
        return barras;
    }

    public void setBarras(String barras) {
        this.barras = barras;
    }

    public String getCodigo() {
        return codigo;
    }

    public void setCodigo(String codigo) {
        this.codigo = codigo;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getUnitario() {
        return unitario;
    }

    public void setUnitario(String unitario) {
        this.unitario = unitario;
    }

    public String getClassFisc() {
        return classFisc;
    }

    public void setClassFisc(String classFisc) {
        this.classFisc = classFisc;
    }

    public String getIcm() {
        return icm;
    }

    public void setIcm(String icm) {
        this.icm = icm;
    }

    public String getGnr() {
        return gnr;
    }

    public void setGnr(String gnr) {
        this.gnr = gnr;
    }

}
